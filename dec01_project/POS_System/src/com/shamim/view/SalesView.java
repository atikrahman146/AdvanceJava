    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        conn = DbConnection.getConnection();
        String sql = "SELECT * FROM sales where mobile = ?";
        String sql2 = "select sum(total) from sales where mobile = ?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField2.getText());
            rs = pst.executeQuery();
            
            pst2 = conn.prepareStatement(sql2);
            pst2.setString(1, jTextField2.getText());
            rs2 = pst2.executeQuery();
            
            jTextArea1.append("Point of Sale"+"\n");
            jTextArea1.append("Customer name: "+jTextField1.getText()+"     Mobile: "+jTextField2.getText()+"\n");
            jTextArea1.append("========================================="+"\n");
            jTextArea1.append("Product Name     Price       Quantity        Total"+"\n");
            jTextArea1.append("--------------------------------------------------------"+"\n");
            while(rs.next()){
                jTextArea1.append(rs.getString(4)+"               "+rs.getDouble(5)+"                  "+rs.getDouble(6)+"                   "+rs.getDouble(7)+"\n");
            }
            jTextArea1.append("--------------------------------------------------------"+"\n");
            while(rs2.next()){
            jTextArea1.append("Total                                                            "+rs2.getDouble(1)+"\n");
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

