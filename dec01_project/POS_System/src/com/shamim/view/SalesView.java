    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        conn = DbConnection.getConnection();
        String sql = "insert into sales(customer_name, mobile, product_name, price, qty, total) values(?, ?, ?, ?, ?, ?);";

        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setDouble(4, Double.parseDouble(jTextField4.getText()));
            pst.setDouble(5, Double.parseDouble(jTextField5.getText()));
            pst.setDouble(6, Double.parseDouble(jTextField6.getText()));
            pst.executeUpdate();
            pst.close();
            conn.close();
            clear();
            JOptionPane.showMessageDialog(null, "Added Successfully");
        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

