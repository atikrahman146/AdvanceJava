    public void clearFormAfterAction() {
        fid.setText("");
        fname.setText("");
    }

    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jFTable.getModel();
        model.setRowCount(0);
        displayStudentWithinModel();
        clearFormAfterAction();
    }

    public void displayStudentWithinModel() {
        studentService = new StudentService();

        List<Student> students = studentService.getStudents();
        DefaultTableModel model = (DefaultTableModel) jFTable.getModel();

        Object[] col = new Object[2];
        for (int i = 0; i < students.size(); i++) {
            col[0] = students.get(i).getId();
            col[1] = students.get(i).getName();
            model.addRow(col);
        }

    }

