    private void fAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fAddButtonActionPerformed
        // TODO add your handling code here:
        studentService = new StudentService();
        Student student = new Student(Integer.parseInt(fid.getText()), fname.getText());

        studentService.save(student);
        fMsg.setText("Record Successfully Saved");

        refreshTable();

    }//GEN-LAST:event_fAddButtonActionPerformed

    private void jFTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jFTableMouseClicked
        // TODO add your handling code here:
        int i = jFTable.getSelectedRow();
        TableModel model = jFTable.getModel();

        fid.setText(model.getValueAt(i, 0).toString());
        fname.setText(model.getValueAt(i, 1).toString());
    }//GEN-LAST:event_jFTableMouseClicked

    private void fUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fUpdateActionPerformed
        // TODO add your handling code here:    
        studentService = new StudentService();
        Student student = new Student(Integer.parseInt(fid.getText()), fname.getText());

        studentService.update(student);
        fMsg.setText("Record Updated");
        refreshTable();
    }//GEN-LAST:event_fUpdateActionPerformed

    private void fDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fDeleteActionPerformed
        // TODO add your handling code here:
        studentService = new StudentService();
        int i = jFTable.getSelectedRow();
        TableModel model = jFTable.getModel();
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        studentService.delete(id);
        fMsg.setText("Record Deleted");
        refreshTable();
    }//GEN-LAST:event_fDeleteActionPerformed

